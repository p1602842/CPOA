package entity;

public class TennisMan {

	public String nom;
	public String prenom;
	public int rang;
	public int age;
	public String nationalite;

	public TennisMan(String nom, String prenom, int rang, int age, String nationalite){

		this.nom = nom;
		this.prenom = prenom;
		this.rang = rang;
		this.age = age;
		this.nationalite = nationalite;
	}
}
